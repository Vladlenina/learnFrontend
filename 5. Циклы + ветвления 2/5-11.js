let n = 10;
for (let i = 0; i < n; i++) {
  let str = '';
  for (let j = 0; j < n; j++) {
    if (i < j && i < n - j - 1) {
      str += '1' + ' ';
    } else if (i < j && i > n - j - 1) {
      str += '2' + ' ';
    } else if (i > j && i > n - j - 1) {
      str += '3' + ' ';
    } else if (i > j && i < n - j - 1) {
      str += '4' + ' ';
    } else {
      str += '0' + ' ';
    }
  }
  console.log(str);
}
